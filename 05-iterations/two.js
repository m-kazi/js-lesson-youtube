// while loop

let i = 0;
while (i <= 10) {
    // console.log(`Value of i: ${i}`);
    i += 2;
}

//on Array
let myArray = ["Flash", "Superman", "Batman"];
let arr = 0;
while (arr < myArray.length) {
    // console.log(myArray[arr]);
    arr++;
}

//do-while loop checks the condition later
let score = 12;
do {
    console.log(`Score is ${score}`);
    score++;
} while (score <= 10);
